// Update staff member (Admin only)
export const updateStaff = asyncHandler(async (req, res) => {
  const { id } = req.params;
  const updates = req.body;

  // Remove sensitive fields that shouldn't be updated directly
  delete updates.password;
  delete updates.loginAttempts;
  delete updates.lockUntil;
  delete updates.createdBy;
  delete updates._id;

  const user = await User.findById(id);
  if (!user) {
    return res.status(404).json({
      success: false,
      message: 'Staff member not found'
    });
  }

  // If role is being updated, validate it
  if (updates.role) {
    const role = await Role.findById(updates.role);
    if (!role || !role.isActive) {
      return res.status(400).json({
        success: false,
        message: 'Invalid or inactive role specified'
      });
    }
    updates.permissions = role.permissions || [];
  }

  // If department is being updated, validate it
  if (updates.department) {
    const department = await Department.findById(updates.department);
    if (!department) {
      return res.status(400).json({
        success: false,
        message: 'Invalid department specified'
      });
    }
  }

  // If the form sent an empty employeeId, keep the existing value as-is so
  // we don't accidentally wipe it.
  if (updates.profile) {
    const incoming = updates.profile.employeeId;
    const existing = user.profile?.employeeId;
    if (!incoming && existing) {
      updates.profile.employeeId = existing;
    }
  }

  // Update user
  Object.assign(user, updates);

  if (!user.profile) user.profile = {};

  // Generate (or upgrade) the employee ID:
  //   - missing → fresh professional id
  //   - legacy format (e.g. `EMP0001`, `ADMIN001`) → migrate to new format
  //   - already professional → leave it alone (it's printed on ID cards)
  const currentId = user.profile.employeeId || '';
  if (!currentId || !PROFESSIONAL_ID_RE.test(currentId)) {
    user.profile.employeeId = await generateNextEmployeeId();
    user.markModified('profile');
  }

  await user.save();

  // Populate updated user
  await user.populate('role department');

  logger.info('Staff member updated by admin', {
    adminId: req.user.id,
    adminUsername: req.user.username,
    updatedUserId: user._id,
    updatedUsername: user.username,
    updates: Object.keys(updates)
  });

  res.json({
    success: true,
    data: user,
    message: 'Staff member updated successfully'
  });
});

// Reset staff password (Admin only)
export const resetStaffPassword = asyncHandler(async (req, res) => {
  const { id } = req.params;
  const { newPassword, generatePassword = false } = req.body;

  const user = await User.findById(id).populate('role');
  if (!user) {
    return res.status(404).json({
      success: false,
      message: 'Staff member not found'
    });
  }

  let finalPassword;
  
  if (generatePassword) {
    // Generate password based on role pattern
    finalPassword = user.role.generatePassword(user.firstName, user.lastName);
  } else {
    if (!newPassword) {
      return res.status(400).json({
        success: false,
        message: 'New password is required when not generating password'
      });
    }
    finalPassword = newPassword;
  }

  // Update password
  user.password = finalPassword;
  await user.save();

  logger.info('Staff password reset by admin', {
    adminId: req.user.id,
    adminUsername: req.user.username,
    targetUserId: user._id,
    targetUsername: user.username
  });

  res.json({
    success: true,
    data: {
      username: user.username,
      newPassword: finalPassword
    },
    message: 'Password reset successfully'
  });
});

// Deactivate/Activate staff member (Admin only)
export const toggleStaffStatus = asyncHandler(async (req, res) => {
  const { id } = req.params;
  const { isActive } = req.body;

  const user = await User.findById(id);
  if (!user) {
    return res.status(404).json({
      success: false,
      message: 'Staff member not found'
    });
  }

  // Prevent admin from deactivating themselves
  if (user._id.toString() === req.user.id) {
    return res.status(400).json({
      success: false,
      message: 'You cannot deactivate your own account'
    });
  }

  user.isActive = isActive;
  await user.save();

  logger.info('Staff status toggled by admin', {
    adminId: req.user.id,
    adminUsername: req.user.username,
    targetUserId: user._id,
    targetUsername: user.username,
    newStatus: isActive ? 'active' : 'inactive'
  });

  res.json({
    success: true,
    data: { isActive: user.isActive },
    message: `Staff member ${isActive ? 'activated' : 'deactivated'} successfully`
  });
});

// Delete staff member (Admin only)
